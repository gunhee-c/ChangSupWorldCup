import streamlit as st

# 지문
text = """
억울하다 억울해 너무나 억울해
돈덜쓰는 리부트 정상화했더니
억울하다 억울해 정말로 억울해
돈안쓰는 놈들은 쌀숭이였다네

메벤남 의견 들어줬을 뿐야
메갤남 여론 반영했을 뿐야
나는 몰랐어 이놈들의 정체
돈안쓰고 쌀먹하는 쌀숭이

리부트 사기라고 외치던 놈들
본섭유입 안될걸 우려한 거지
자기템 설거지 못시킬 걱정이
쌀숭이들의 본심이었던 거지
"""

st.title("메이플 디렉터 신창섭의 억울한 심정")

# 지문을 보여주는 부분
with st.expander("지문 보기"):
    st.write(text)

# 문제 출제 및 정답 확인을 모듈화
def ask_question(question, options, correct_option):
    st.subheader("문제")
    st.write(question)
    answer = st.radio("정답을 선택하세요:", options)
    
    if answer == correct_option:
        st.write("정답입니다!")
    else:
        st.write("오답입니다. 다시 시도해 보세요.")

# 첫 번째 문제
question_1 = """
다음 글을 바탕으로, 글쓴이의 주장에 대해 가장 적절한 것은?
"""

options_1 = [
    "1. 메이플스토리의 본섭 유저들은 리부트 유저들을 도와주기 위해 노력하고 있다.",
    "2. 글쓴이는 리부트 서버 유저들의 게임 플레이 방식에 대해 긍정적으로 평가하고 있다.",
    "3. 글쓴이는 리부트 서버의 경제 정상화를 추진했으나, 이에 대한 불만이 존재한다고 주장한다.",
    "4. 글쓴이는 리부트 서버의 과금 시스템이 본섭보다 공정하다고 주장한다.",
    "5. 본섭 유저들은 리부트 서버 유저들의 경제적 이익을 위해 협력하고 있다고 글쓴이는 주장한다."
]

ask_question(question_1, options_1, "3. 글쓴이는 리부트 서버의 경제 정상화를 추진했으나, 이에 대한 불만이 존재한다고 주장한다.")

